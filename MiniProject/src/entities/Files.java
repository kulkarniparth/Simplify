package entities;

public class Files {
	private String id;
	private String title;
	private String file;
	private String student_id;
	
	
	
	public String getFile_id() {
		return id;
	}
	
	public void setFile_id(String id) {
		this.id = id;
	}
	
	
	public String getFile_title() {
		return title;
	}
	
	public void setFile_title(String title) {
		this.title = title;
	}
	
	public String getFile_file() {
		return file;
	}
	
	public void setFile_file(String file) {
		this.file = file;
	}
	
	public String getStudent_id() {
		return student_id;
	}
	
	
	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}

}
